package ch02;

public class LongExample {

	public static void main(String[] args) {
		long var1 = 10;
		long var2 = 20L;
		// long var3 = 100000000000; int타입 저장범위 넘어갈땐 뒤에 L 붙이기
		long var4 = 100000000000L;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var4);

	}

}
